package com.pesantren.boardingschool.model.data;

public class Aliran {

    String aliran, namaAliran;

    public Aliran(String aliran, String namaAliran) {
        this.aliran = aliran;
        this.namaAliran = namaAliran;
    }

    public String getAliran() {
        return aliran;
    }

    public String getNamaAliran() {
        return namaAliran;
    }
}
